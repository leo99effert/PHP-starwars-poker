<p>In Sabacc the goal is to get a score as close to 23 or -23 as possible without going over.</p>
<p>It is also possible to get a hand that is even better than 23 and -23 called 'The Idiots Array'.</p>
<p>You achieve it by having these cards in your hand: a zero, a two or minus two, and a three or minus three.</p>
<p>You then spell 023 instead of getting it by adding. </p>
